    public void verificarVitoria(){
        for(Player p:AppManager.INSTANCE.getPlayers()){
            if(p.getPoint() >= 200){
                showVitory(p);
            }
        }
    }

    private void showVitory(Player p) {
        String message =p.getName() + " wins the game";
        JOptionPane.showMessageDialog(this, message);
    }

